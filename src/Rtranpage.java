    public void autosum() {
        int ttl = 0;
        for (int i = 0; i < RTtable.getRowCount(); i++) {
            int amount = Integer.parseInt((String) RTtable.getValueAt(i, 3));
            ttl += amount;
        }
        jLabel4.setText("" + ttl);
        angka = Integer.parseInt(jLabel4.getText());
        ganti = NumberFormat.getNumberInstance(Locale.US).format(angka);
        token = new StringTokenizer(ganti, ".");
        ganti = token.nextToken();
        ganti = ganti.replace(',', '.');
        jLabel4.setText("Rp. " + String.format(ganti));
    }
