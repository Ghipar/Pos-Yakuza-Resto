    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        // TODO add your handling code here:
        try {
            String sql = "select * from transaksi where "
                    + "Kode_Transaksi ='" + Kt.getText() + "' || Nama_Pembeli = '" + Kt1.getText() + "' || Tanggal_Transaksi = '" + Kt2.getText() + "' ";

            java.sql.Connection conn = (Connection) Config.configDB();
            java.sql.PreparedStatement stm = conn.prepareStatement(sql);
            java.sql.ResultSet res = stm.executeQuery();
            DefaultTableModel tbl = new DefaultTableModel();
            tbl.addColumn("Kode_Tran");
            tbl.addColumn("Nama_Pembeli");
            tbl.addColumn("No Meja");
            tbl.addColumn("Grand_Total");
            tbl.addColumn("Tanggal_Tran");
            RTtable.setModel(tbl);
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("Kode_Transaksi"),
                    res.getString("Nama_Pembeli"),
                    res.getString("No_Meja"),
                    res.getString("Grand_Total"),
                    res.getString("Tanggal_Transaksi")
                });
                RTtable.setModel(tbl);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "salah");

        }
    }//GEN-LAST:event_jLabel6MouseClicked

